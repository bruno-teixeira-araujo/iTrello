package org.arago.showcase.model.rest;

import org.hibernate.validator.constraints.NotEmpty;

public class Board {
	@NotEmpty
	private String id;
	private String name;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
}
